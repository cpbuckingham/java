
public class do_while_loops {

	public static void main(String[] args) {
		int counter = 15;
		
		do{
			System.out.println(counter);
			counter++;
		}while(counter <=10);
	}

}

//anytime you wanna do something at least once, 
//you use a do loop.
//a do while loop guarantees at least one run of 
//your loop and a while loop only runs 
//it when the test is true